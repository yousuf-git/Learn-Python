# 01- Arithmetic Operators
a = 10
b = 4
print("Sum:", a+b)
# print(type(a+b)) # int
print("Diff:", a-b)
print("Product:", a*b)
print("Div:", a/b)
# print(type(a/b)) # float
print("Modulus:", a%b)
print("Power:", a**b)

# 02- Relational operators
print("a==b", a==b)
print("a>b", a>b)
print("a<b", a<b)
print("a>=b", a>=b)
print("a<=b", a<=b)
print("a!=b", a!=b)

